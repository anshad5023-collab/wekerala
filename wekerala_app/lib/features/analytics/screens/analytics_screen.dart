// ─── Peak Hours Bar Chart ─────────────────────────────────────────────────────

class _PeakHoursChart extends StatelessWidget {
  final List<int> hourCounts;
  const _PeakHoursChart({required this.hourCounts});

  @override
  Widget build(BuildContext context) {
    final maxVal =
        hourCounts.reduce((a, b) => a > b ? a : b).toDouble();

    return Container(
      height: 180,
      padding: const EdgeInsets.fromLTRB(8, 12, 12, 8),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(10),
        border: Border.all(color: Colors.grey.shade200),
      ),
      child: BarChart(
        BarChartData(
          maxY: maxVal < 1 ? 1 : maxVal + 1,
          gridData: const FlGridData(show: false),
          borderData: FlBorderData(show: false),
          titlesData: FlTitlesData(
            leftTitles:
                const AxisTitles(sideTitles: SideTitles(showTitles: false)),
            topTitles:
                const AxisTitles(sideTitles: SideTitles(showTitles: false)),
            rightTitles:
                const AxisTitles(sideTitles: SideTitles(showTitles: false)),
            bottomTitles: AxisTitles(
              sideTitles: SideTitles(
                showTitles: true,
                getTitlesWidget: (val, _) {
                  final h = val.toInt();
                  if (h % 6 != 0) return const SizedBox.shrink();
                  final label = h == 0
                      ? '12a'
                      : h < 12
                          ? '${h}a'
                          : h == 12
                              ? '12p'
                              : '${h - 12}p';
                  return Text(label,
                      style: const TextStyle(
                          fontSize: 10, color: AppColors.textSecondary));
                },
              ),
            ),
          ),
          barGroups: List.generate(
              24,
              (h) => BarChartGroupData(
                    x: h,
                    barRods: [
                      BarChartRodData(
                        toY: hourCounts[h].toDouble(),
                        color: AppColors.primary,
                        width: 8,
                        borderRadius: BorderRadius.circular(3),
                      ),
                    ],
                  )),
        ),
      ),
    );
  }
}
