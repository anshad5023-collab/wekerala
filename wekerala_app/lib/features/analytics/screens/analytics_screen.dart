class AnalyticsScreen extends ConsumerWidget {
  const AnalyticsScreen({super.key});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final translations = ref.watch(translationsProvider);
    String t(String key) => translations[key] ?? key;
    final shopAsync = ref.watch(activeShopIdProvider);

    return shopAsync.when(
      loading: () => const Scaffold(body: ShimmerList(itemCount: 3, itemHeight: 80)),
      error: (e, _) => Center(child: Text(e.toString())),
      data: (shopId) {
        if (shopId == null) return Center(child: Text(t('error_generic')));
        return _AnalyticsBody(shopId: shopId, t: t);
      },
    );
  }
}

class _AnalyticsBody extends ConsumerStatefulWidget {
  final String shopId;
  final String Function(String) t;
  const _AnalyticsBody({required this.shopId, required this.t});

  @override
  ConsumerState<_AnalyticsBody> createState() => _AnalyticsBodyState();
}

class _AnalyticsBodyState extends ConsumerState<_AnalyticsBody> {
  bool _sendingWhatsApp = false;

  // Cached analytics for share report
  double _revenue = 0;
  int _orderCount = 0;
  int _completedCount = 0;
  int _cancelledCount = 0;
  String _topProduct = '';
  String _currentPeriod = 'Today';

  void _updateAnalytics({
    required double revenue,
    required int orderCount,
    required int completedCount,
    required int cancelledCount,
    required String topProduct,
    required String period,
  }) {
    if (!mounted) return;
    setState(() {
      _revenue = revenue;
      _orderCount = orderCount;
      _completedCount = completedCount;
      _cancelledCount = cancelledCount;
      _topProduct = topProduct;
      _currentPeriod = period;
    });
  }

  void _shareReport() {
    final completionRate = _orderCount > 0
        ? (_completedCount / _orderCount * 100).toStringAsFixed(0)
        : '0';
    final text = '''📊 *Oratas Shop Report*
📅 $_currentPeriod
─────────────────
💰 Revenue: ₹${_revenue.toStringAsFixed(0)}
📦 Orders: $_orderCount
✅ Completed: $_completedCount
❌ Cancelled: $_cancelledCount
📈 Completion Rate: $completionRate%
─────────────────
${_topProduct.isNotEmpty ? '🏆 Top Product: $_topProduct\n─────────────────\n' : ''}Generated by Oratas''';
    Share.share(text);
  }

  Future<void> _sendSummaryToWhatsApp() async {
    setState(() => _sendingWhatsApp = true);
    try {
      final shopAsync = ref.read(shopStreamProvider(widget.shopId));
      final shop = shopAsync.valueOrNull;
      final shopName = shop?.shopName ?? 'My Shop';

      final summary =
          await SalesSummaryService.getTodaySummary(widget.shopId);
      final text =
          SalesSummaryService.formatSummaryText(summary, shopName);
      final encoded = Uri.encodeComponent(text);

      final phone =
          (shop?.ownerWhatsApp ?? '').replaceAll(RegExp(r'[^0-9]'), '');
      final url = phone.isNotEmpty
          ? 'https://wa.me/91$phone?text=$encoded'
          : 'https://wa.me/?text=$encoded';

      await launchUrl(Uri.parse(url), mode: LaunchMode.externalApplication);
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
              content: Text('Error: $e'), backgroundColor: Colors.red),
        );
      }
    } finally {
      if (mounted) setState(() => _sendingWhatsApp = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    final ordersAsync = ref.watch(ordersStreamProvider(widget.shopId));

    return Scaffold(
      backgroundColor: AppColors.background,
      appBar: AppBar(
        title: Text(widget.t('analytics_title')),
        backgroundColor: AppColors.primary,
        foregroundColor: Colors.white,
        actions: [
          IconButton(
            icon: const Icon(Icons.share_outlined),
            tooltip: 'Share Report',
            onPressed: _shareReport,
          ),
          Padding(
            padding: const EdgeInsets.only(right: 8),
            child: _sendingWhatsApp
                ? const Center(
                    child: SizedBox(
                      width: 22,
                      height: 22,
                      child: CircularProgressIndicator(
                          color: Colors.white, strokeWidth: 2),
                    ),
                  )
                : IconButton(
                    tooltip: 'Send to WhatsApp',
                    icon: const Icon(Icons.send),
                    onPressed: _sendSummaryToWhatsApp,
                  ),
          ),
        ],
      ),
      body: Column(
        children: [
          // Prominent WhatsApp summary button
          Padding(
            padding: const EdgeInsets.fromLTRB(16, 12, 16, 0),
            child: SizedBox(
              width: double.infinity,
              child: ElevatedButton.icon(
                icon: _sendingWhatsApp
                    ? const SizedBox(
                        width: 18,
                        height: 18,
                        child: CircularProgressIndicator(
                            color: Colors.white, strokeWidth: 2),
                      )
                    : const Icon(Icons.send, size: 18),
                label: const Text('Send Today\'s Summary to WhatsApp'),
                style: ElevatedButton.styleFrom(
                  backgroundColor: const Color(0xFF25D366),
                  foregroundColor: Colors.white,
                  padding: const EdgeInsets.symmetric(vertical: 12),
                  shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(10)),
                ),
                onPressed: _sendingWhatsApp ? null : _sendSummaryToWhatsApp,
              ),
            ),
          ),
          const SizedBox(height: 8),
          Expanded(
            child: _CombinedAnalytics(
              shopId: widget.shopId,
              ordersAsync: ordersAsync,
              t: widget.t,
              onAnalyticsReady: _updateAnalytics,
            ),
          ),
        ],
      ),
    );
  }
}

// Merges online orders + POS bills and renders _AnalyticsContent
class _CombinedAnalytics extends ConsumerWidget {
  final String shopId;
  final AsyncValue<List<OrderModel>> ordersAsync;
  final String Function(String) t;
  final void Function({
    required double revenue,
    required int orderCount,
    required int completedCount,
    required int cancelledCount,
    required String topProduct,
    required String period,
  }) onAnalyticsReady;

  const _CombinedAnalytics({
    required this.shopId,
    required this.ordersAsync,
    required this.t,
    required this.onAnalyticsReady,
  });

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final billsAsync = ref.watch(weeklyBillsProvider(shopId));

    return ordersAsync.when(
      loading: () => const ShimmerList(itemCount: 3, itemHeight: 80),
      error: (e, _) => Center(child: Text(e.toString())),
      data: (orders) => billsAsync.when(
        loading: () => _AnalyticsContent(
            orders: orders, bills: const [], t: t, onAnalyticsReady: onAnalyticsReady),
        error: (_, __) => _AnalyticsContent(
            orders: orders, bills: const [], t: t, onAnalyticsReady: onAnalyticsReady),
        data: (bills) => _AnalyticsContent(
            orders: orders, bills: bills, t: t, onAnalyticsReady: onAnalyticsReady),
      ),
    );
  }
}

class _AnalyticsContent extends StatelessWidget {
  final List<OrderModel> orders;
  final List<BillModel> bills;
  final String Function(String) t;
  final void Function({
    required double revenue,
    required int orderCount,
    required int completedCount,
    required int cancelledCount,
    required String topProduct,
    required String period,
  }) onAnalyticsReady;

  const _AnalyticsContent({
    required this.orders,
    required this.bills,
    required this.t,
    required this.onAnalyticsReady,
  });

  @override
  Widget build(BuildContext context) {
    final now = DateTime.now();
    final todayStart = DateTime(now.year, now.month, now.day);
    final weekStart = todayStart.subtract(Duration(days: now.weekday - 1));
    final monthStart = DateTime(now.year, now.month, 1);

    final todayOrders =
        orders.where((o) => o.createdAt.isAfter(todayStart)).toList();
    final weekOrders =
        orders.where((o) => o.createdAt.isAfter(weekStart)).toList();
    final monthOrders =
        orders.where((o) => o.createdAt.isAfter(monthStart)).toList();

    // POS bills — merged into revenue numbers
    final todayBills =
        bills.where((b) => b.createdAt.isAfter(todayStart) && !b.isVoided).toList();
    final weekBills =
        bills.where((b) => b.createdAt.isAfter(weekStart) && !b.isVoided).toList();
    final monthBills =
        bills.where((b) => b.createdAt.isAfter(monthStart) && !b.isVoided).toList();

    final todayRevenue = todayOrders.fold(0.0, (s, o) => s + o.totalAmount)
        + todayBills.fold(0.0, (s, b) => s + b.finalAmount);
    final weekRevenue = weekOrders.fold(0.0, (s, o) => s + o.totalAmount)
        + weekBills.fold(0.0, (s, b) => s + b.finalAmount);
    final monthRevenue = monthOrders.fold(0.0, (s, o) => s + o.totalAmount)
        + monthBills.fold(0.0, (s, b) => s + b.finalAmount);

    final deliveredMonth =
        monthOrders.where((o) => o.status == 'delivered').length;
    final completionRate =
        monthOrders.isEmpty ? 0.0 : deliveredMonth / monthOrders.length * 100;

    // 7-day revenue sparkline — orders + bills combined
    final last7 = List.generate(7, (i) {
      final day = DateTime.now().subtract(Duration(days: 6 - i));
      final dayStart = DateTime(day.year, day.month, day.day);
      final dayEnd = dayStart.add(const Duration(days: 1));
      final orderRev = orders
          .where((o) =>
              o.createdAt.isAfter(dayStart) && o.createdAt.isBefore(dayEnd))
          .fold(0.0, (s, o) => s + o.totalAmount);
      final billRev = bills
          .where((b) =>
              !b.isVoided &&
              b.createdAt.isAfter(dayStart) &&
              b.createdAt.isBefore(dayEnd))
          .fold(0.0, (s, b) => s + b.finalAmount);
      return orderRev + billRev;
    });

    // Top 5 products this week — orders + bills
    final productCounts = <String, int>{};
    for (final o in weekOrders) {
      for (final item in o.items) {
        productCounts[item.productName] =
            (productCounts[item.productName] ?? 0) + 1;
      }
    }
    for (final b in weekBills) {
      for (final item in b.items) {
        productCounts[item.productName] =
            (productCounts[item.productName] ?? 0) + item.qty.toInt();
      }
    }
    final topProducts = productCounts.entries.toList()
      ..sort((a, b) => b.value.compareTo(a.value));
    final top5 = topProducts.take(5).toList();

    // Max count for proportional progress bars in product tiles
    final maxProductCount = top5.isEmpty ? 1 : top5.first.value;

    // Notify parent with computed analytics for share report
    WidgetsBinding.instance.addPostFrameCallback((_) {
      onAnalyticsReady(
        revenue: todayRevenue,
        orderCount: todayOrders.length + todayBills.length,
        completedCount: todayOrders.where((o) => o.status == 'delivered').length,
        cancelledCount: todayOrders.where((o) => o.status == 'cancelled').length,
        topProduct: top5.isNotEmpty ? '${top5.first.key} (${top5.first.value} sold)' : '',
        period: 'Today',
      );
    });

    // Peak hours this week (0-23) — orders + bills
    final hourCounts = List<int>.filled(24, 0);
    for (final o in weekOrders) {
      hourCounts[o.createdAt.hour]++;
    }
    for (final b in weekBills) {
      hourCounts[b.createdAt.hour]++;
    }

    return ListView(
      padding: const EdgeInsets.all(16),
      children: [
        // Hero KPI tiles
        _SectionTitle(t('analytics_overview')),
        const SizedBox(height: 8),
        Row(children: [
          _KpiCard(
            label: t('analytics_today'),
            value: '₹${todayRevenue.toInt()}',
            subValue: '${todayOrders.length + todayBills.length} sales',
            color: AppColors.primary,
          ),
          const SizedBox(width: 8),
          _KpiCard(
            label: t('analytics_week'),
            value: '₹${weekRevenue.toInt()}',
            subValue: '${weekOrders.length + weekBills.length} sales',
            color: const Color(0xFF1976D2),
          ),
          const SizedBox(width: 8),
          _KpiCard(
            label: t('analytics_month'),
            value: '₹${monthRevenue.toInt()}',
            subValue: '${monthOrders.length + monthBills.length} sales',
            color: AppColors.success,
          ),
        ]),
        const SizedBox(height: 12),
        _CompletionCard(rate: completionRate, t: t),
        const SizedBox(height: 12),
        _SparklineChart(values: last7),
        const SizedBox(height: 16),

        // Top products
        _SectionTitle(t('analytics_top_products')),
        const SizedBox(height: 8),
        if (top5.isEmpty)
          _EmptyCard(t('analytics_no_data'))
        else
          ...top5.asMap().entries.map((e) => _ProductRankTile(
                rank: e.key + 1,
                name: e.value.key,
                count: e.value.value,
                maxCount: maxProductCount,
              )),
        const SizedBox(height: 16),

        // Peak hours chart
        _SectionTitle(t('analytics_peak_hours')),
        const SizedBox(height: 8),
        _PeakHoursChart(hourCounts: hourCounts),
        const SizedBox(height: 20),
      ],
    );
  }
}

