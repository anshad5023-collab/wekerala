// ─── Product Rank Tile (with proportional bar) ───────────────────────────────

class _ProductRankTile extends StatelessWidget {
  final int rank;
  final String name;
  final int count;
  final int maxCount;
  const _ProductRankTile(
      {required this.rank,
      required this.name,
      required this.count,
      required this.maxCount});

  @override
  Widget build(BuildContext context) {
    final fraction = maxCount > 0 ? count / maxCount : 0.0;
    return Container(
      margin: const EdgeInsets.only(bottom: 8),
      padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 12),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(10),
        border: Border.all(color: Colors.grey.shade200),
      ),
      child: Column(
        children: [
          Row(children: [
            // Rank badge
            Container(
              width: 26,
              height: 26,
              decoration: BoxDecoration(
                color: rank == 1
                    ? Colors.amber
                    : AppColors.primary.withValues(alpha: 0.1),
                shape: BoxShape.circle,
              ),
              child: Center(
                child: Text('$rank',
                    style: TextStyle(
                        fontSize: 11,
                        fontWeight: FontWeight.bold,
                        color:
                            rank == 1 ? Colors.white : AppColors.primary)),
              ),
            ),
            const SizedBox(width: 10),
            Expanded(
                child: Text(name,
                    style: const TextStyle(fontWeight: FontWeight.w500))),
            Text('$count orders',
                style: const TextStyle(
                    color: AppColors.textSecondary, fontSize: 12)),
          ]),
          const SizedBox(height: 8),
          // Proportional popularity bar
          ClipRRect(
            borderRadius: BorderRadius.circular(4),
            child: LinearProgressIndicator(
              value: fraction,
              minHeight: 5,
              backgroundColor: Colors.grey.shade100,
              valueColor: AlwaysStoppedAnimation<Color>(
                rank == 1 ? Colors.amber.shade600 : AppColors.primaryLight,
              ),
            ),
          ),
        ],
      ),
    );
  }
}

